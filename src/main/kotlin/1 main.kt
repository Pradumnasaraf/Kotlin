
// Main function ,fun() stands for Function
fun main() {

    //print is use for printing on the same line
    print("HEY  ")

    // println is use for printing anything
    println("Hello World!")



    // var is mutable
    var x : Int = 3
    print(x)

    // to concatenate we use $ symbol
    println("Value of my variable is $x")

    x = 4 // var can be updated
    println("Value of my variable is $x")

    // val is immutable (i.e we can't the variable value in the future)
    val y :Int =5
    // y = 6 Not possible due to its immutable in nature

    val z = 5 // Both way of writing is correct and acceptable.
    val wholeNumber = 12
    val bigNumber = 12L //long
    val preciseDecimal  = 3.232
    val decimal = 3.33f //floating point number
    val iLoveApple = true //boolean
    val iAmLetter = 'A'
    
    println(y)
    println(z)









}